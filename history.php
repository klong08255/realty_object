            อีกแปลงหนึ่งอยู่ทางทิศตะวันตกใช้เป็นเขตฌาปนสถาน มีเนื้อที่ 9 ไร่ 1 งาน 11 ตารางวา ภายในบริเวณวัดมีถนนติดต่อระหว่างอาคารเสนาสนะต่าง ๆ ถึงกันหมด
