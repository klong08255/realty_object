                <h5>วัดนครสวรรค์ เดิมมีนามว่า “วัดหัวเมือง” เพราะตั้งอยู่ตอนต้นของตัวเมืองก่อนจะเข้าถึงตัวเมืองจะต้องผ่านวัดนี้ก่อน สร้างขึ้นในราว พ.ศ.1972 
