            วัดนครสวรรค์ เป็นพระอารามหลวงชั้นตรี ชนิดสามัญ ตั้งอยู่เลขที่ 702 ถนนโกสีย์ ตำบลปากน้ำโพ อำเภอเมืองนครสวรรค์ จังหวัดนครสวรรค์ สังกัดคณะสงฆ์มหานิกาย 
