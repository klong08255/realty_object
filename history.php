            ตำบลศาลาแดง อำเภอโกรกพระ 1 แปลง และตำบลเนินมะกอก อำเภอโกรกพระ จังหวัดนครสวรรค์ 1 แปลง
            พื้นที่ตั้งวัดเป็นที่ราบ มีลักษณะเป็นรูปสี่เหลี่ยมคางหมู มีกำแพงโดยรอบทั้ง 4 ด้าน และมีประตูเข้าออกได้สะดวกทั้ง 4 ด้าน เช่นกัน 
